    public Bitmap getBitmap(){
       //*****************MAY Already be Done As Part of VUFORIA Initialization*******
        Vuforia.setFrameFormat(PIXEL_FORMAT.RGB565, true); //enables RGB565 format for the image
        vuforia.setFrameQueueCapacity(4); //tells VuforiaLocalizer to only store one frame at a time

        /*To access the image: you need to iterate through the images of the frame object:*/
        Image rgb = null;
        VuforiaLocalizer.CloseableFrame frame=null;
        ElapsedTime timer = new ElapsedTime();
        timer.reset();
        while(timer.seconds() < 2){
            try{

                frame = vuforia.getFrameQueue().take(); //takes the frame at the head of the queue
            }catch (InterruptedException e){
                e.printStackTrace();
            }
            int numImages=(int)frame.getNumImages();
            for (int i = 0; i < numImages; i++) {
                if (frame.getImage(i).getFormat() == PIXEL_FORMAT.RGB565) {
                    rgb = frame.getImage(i);
                    break;
                }//if
            }

            if(rgb != null)
                break;

        }

        //first create a bitmap

        Bitmap bm = Bitmap.createBitmap(rgb.getWidth(), rgb.getHeight(),
                Bitmap.Config.RGB_565);
        bm.copyPixelsFromBuffer(rgb.getPixels());




        //frame.close();
        return bm;
    }

    public RobotPosition getCurrentPosition(boolean isStop){
        RobotPosition robotPosition = new RobotPosition();

        timer.reset();

        targetsSkyStone.activate();
        while(timer.time() <= 1 && !isStop){

            // check all the trackable targets to see which one (if any) is visible.
            targetVisible = false;
            for (VuforiaTrackable trackable : allTrackables) {
                if (((VuforiaTrackableDefaultListener)trackable.getListener()).isVisible()) {
//                        telemetry.addData("Visible Target", trackable.getName());
                    targetVisible = true;

                    // getUpdatedRobotLocation() will return null if no new information is available since
                    // the last time that call was made, or if the trackable is not currently visible.
                    OpenGLMatrix robotLocationTransform = ((VuforiaTrackableDefaultListener)trackable.getListener()).getUpdatedRobotLocation();
                    if (robotLocationTransform != null) {
                        lastLocation = robotLocationTransform;
                    }
                    break;
                }
            }

            // Provide feedback as to where the robot is located (if we know).
            if (targetVisible) {
                // express position (translation) of robot in inches.
                VectorF translation = lastLocation.getTranslation();
//                    telemetry.addData("Pos (in)", "{X, Y, Z} = %.1f, %.1f, %.1f",
//                            translation.get(0) / mmPerInch, translation.get(1) / mmPerInch, translation.get(2) / mmPerInch);
                robotPosition.x = translation.get(0)/mmPerInch;
                robotPosition.y = translation.get(1)/mmPerInch;




                // express the rotation of the robot in degrees.
                Orientation rotation = Orientation.getOrientation(lastLocation, EXTRINSIC, XYZ, DEGREES);
                // telemetry.addData("Rot (deg)", "{Roll, Pitch, Heading} = %.0f, %.0f, %.0f", rotation.firstAngle, rotation.secondAngle, rotation.thirdAngle);

                robotPosition.rot = rotation.firstAngle - 90.0;


            }
            else {
                //telemetry.addData("Visible Target", "none");
                return null;
            }
//            telemetry.update();

        }
        // Disable Tracking when we are done;
        targetsSkyStone.deactivate();



        return robotPosition;
    }




